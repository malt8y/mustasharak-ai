const OPENROUTER_MODEL = "deepseek/deepseek-r1-0528";

async function streamFromOpenRouter(
  messages: { role: string; content: string }[],
  res: Response,
  finished: () => boolean
) {
  const apiKey = process.env.OPENROUTER_API_KEY;

  if (!apiKey) {
    const response = await invokeLLM({ messages: messages as any });
    const content = response.choices?.[0]?.message?.content || "عذرًا، حدث خطأ.";
    res.write("data: " + JSON.stringify({ choices: [{ delta: { content } }] }) + "\n\n");
    res.write("data: [DONE]\n\n");
    return res.end();
  }

  const orController = new AbortController();
  const orTimeout = setTimeout(() => orController.abort(), 55000);

  let streamResponse: globalThis.Response;
  try {
    streamResponse = await fetch("https://openrouter.ai/api/v1/chat/completions", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        Authorization: `Bearer ${apiKey}`,
        "HTTP-Referer": "https://maaka-ai.sa",
        "X-Title": "Maaka AI",
      },
      body: JSON.stringify({
        model: OPENROUTER_MODEL,
        messages,
        stream: true,
        temperature: 0.6,
        max_tokens: 1800,
      }),
      signal: orController.signal,
    });
  } catch (fetchErr) {
    clearTimeout(orTimeout);
    console.error("OpenRouter fetch error:", fetchErr);
    const response = await invokeLLM({ messages: messages as any });
    const content = response.choices?.[0]?.message?.content || "عذرًا، حدث خطأ في الاتصال.";
    res.write("data: " + JSON.stringify({ choices: [{ delta: { content } }] }) + "\n\n");
    res.write("data: [DONE]\n\n");
    return res.end();
  }
  clearTimeout(orTimeout);

  if (!streamResponse.ok) {
    const err = await streamResponse.text();
    console.error("OpenRouter error:", err);
    const response = await invokeLLM({ messages: messages as any });
    const content = response.choices?.[0]?.message?.content || "عذرًا، حدث خطأ في الاتصال.";
    res.write("data: " + JSON.stringify({ choices: [{ delta: { content } }] }) + "\n\n");
    res.write("data: [DONE]\n\n");
    return res.end();
  }

  const reader = streamResponse.body?.getReader();
  const decoder = new TextDecoder();

  if (!reader) {
    res.write("data: [DONE]\n\n");
    return res.end();
  }

  while (!finished()) {
    const { done, value } = await reader.read();
    if (done) break;
    const chunk = decoder.decode(value, { stream: true });
    for (const line of chunk.split("\n")) {
      if (finished()) break;
      if (!line.startsWith("data: ")) continue;
      const data = line.slice(6).trim();
      if (data === "[DONE]") {
        res.write("data: [DONE]\n\n");
        break;
      }
      try {
        const parsed = JSON.parse(data);
        const delta = parsed.choices?.[0]?.delta;
        if (delta?.content) {
          const clean = { choices: [{ delta: { content: delta.content } }] };
          res.write("data: " + JSON.stringify(clean) + "\n\n");
        }
      } catch {
        // skip malformed chunks
      }
    }
  }

  if (!finished()) {
    res.write("data: [DONE]\n\n");
    res.end();
  }
}

// ===== Handler =====
export async function chatHandler(req: Request, res: Response) {
  try {
    const ip =
      req.headers["x-forwarded-for"]?.toString().split(",")[0]?.trim() ||
      req.socket.remoteAddress ||
      "unknown";

    if (!checkRateLimit(ip)) {
      return res.status(429).json({
        error: "تجاوزت الحد المسموح. انتظر دقيقة وحاول مرة أخرى.",
      });
    }

    const { message, history = [] } = req.body;

    if (!message || typeof message !== "string" || message.length > 2000) {
      return res.status(400).json({ error: "الرسالة غير صالحة" });
    }

    const messages = [
      { role: "system", content: SYSTEM_PROMPT },
      ...history.slice(-12).map((m: { role: string; content: string }) => ({
        role: m.role,
        content: m.content,
      })),
      { role: "user", content: message },
    ];

    res.setHeader("Content-Type", "text/event-stream");
    res.setHeader("Cache-Control", "no-cache");
    res.setHeader("Connection", "keep-alive");
    res.setHeader("X-Accel-Buffering", "no");
    res.setHeader("Transfer-Encoding", "chunked");

    if (typeof (res as any).flushHeaders === "function") {
      (res as any).flushHeaders();
    }
    res.write("data: {\"ping\":true}\n\n");

    let _finished = false;
    const cleanup = () => { _finished = true; };
    res.on("close", cleanup);
    res.on("finish", cleanup);

    await streamFromOpenRouter(messages, res, () => _finished);
  } catch (error) {
    console.error("Chat handler error:", error);
    if (!res.headersSent) {
      res.status(500).json({ error: "حدث خطأ في السيرفر" });
    }
  }
}
